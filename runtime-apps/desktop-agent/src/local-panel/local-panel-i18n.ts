export function getLocalPanelMessages(locale: LocalPanelLocale): LocalPanelMessages {
  return LOCAL_PANEL_TRANSLATIONS[locale];
}

export function getLocalPanelClientConfig(): LocalPanelClientConfig {
  return {
    defaultLocale: LOCAL_PANEL_DEFAULT_LOCALE,
    storageKey: LOCAL_PANEL_STORAGE_KEY,
    supportedLocales: LOCAL_PANEL_SUPPORTED_LOCALES,
    localeAliases: LOCAL_PANEL_LOCALE_ALIASES,
    localeNames: LOCAL_PANEL_LOCALE_NAMES,
    translations: LOCAL_PANEL_TRANSLATIONS
  };
}

export function resolveLocalPanelLocale(
  preferredLocales: string | readonly string[] | null | undefined
): LocalPanelLocale {
  const candidates = Array.isArray(preferredLocales)
    ? preferredLocales
    : typeof preferredLocales === "string"
      ? preferredLocales.split(",")
      : [];

  for (const rawCandidate of candidates) {
    const normalized = rawCandidate.split(";")[0]?.trim().toLowerCase();
    if (!normalized) {
      continue;
    }

    const directMatch = LOCAL_PANEL_SUPPORTED_LOCALES.find((locale) => locale.toLowerCase() === normalized);
    if (directMatch) {
      return directMatch;
    }

    const aliasedMatch = LOCAL_PANEL_LOCALE_ALIASES[normalized];
    if (aliasedMatch) {
      return aliasedMatch;
    }
  }

  return LOCAL_PANEL_DEFAULT_LOCALE;
}

NODE_ENV=production
PORT=${RELAY_PORT}
RELAY_BIND_HOST=127.0.0.1
RELAY_PUBLIC_BASE_URL=https://${RELAY_DOMAIN}
DATABASE_URL=postgres://${DB_USER}:${DB_PASSWORD}@127.0.0.1:5432/${DB_NAME}
REDIS_URL=redis://127.0.0.1:6379
RELAY_ENABLE_DEV_RESET=0
[setup][dry-run] ============================
EOF
    return
  fi

  cat > "${RELAY_ENV_FILE}" <<EOF
NODE_ENV=production
PORT=${RELAY_PORT}
RELAY_BIND_HOST=127.0.0.1
RELAY_PUBLIC_BASE_URL=https://${RELAY_DOMAIN}
DATABASE_URL=postgres://${DB_USER}:${DB_PASSWORD}@127.0.0.1:5432/${DB_NAME}
REDIS_URL=redis://127.0.0.1:6379
RELAY_ENABLE_DEV_RESET=0
EOF
  chown root:"${APP_GROUP}" "${RELAY_ENV_FILE}"
  chmod 0640 "${RELAY_ENV_FILE}"
}


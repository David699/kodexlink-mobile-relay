#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
CHECK_SCRIPT="${ROOT_DIR}/scripts/check-ubuntu-relay-host.sh"

NODE_MAJOR="${NODE_MAJOR:-24}"
PNPM_VERSION="${PNPM_VERSION:-10.6.0}"
APP_USER="${APP_USER:-kodexlink}"
APP_GROUP="${APP_GROUP:-kodexlink}"
APP_DIR="${APP_DIR:-/srv/kodexlink}"
ETC_DIR="${ETC_DIR:-/etc/kodexlink}"
LOG_DIR="${LOG_DIR:-/var/log/kodexlink}"
RELAY_PORT="${RELAY_PORT:-8787}"
RELAY_DOMAIN="${RELAY_DOMAIN:-}"
SSL_CERT_PATH="${SSL_CERT_PATH:-}"
SSL_KEY_PATH="${SSL_KEY_PATH:-}"
DB_NAME="${DB_NAME:-codex_mobile}"
DB_USER="${DB_USER:-kodexlink}"
DB_PASSWORD="${DB_PASSWORD:-}"
SSH_UFW_PROFILE="${SSH_UFW_PROFILE:-OpenSSH}"
ENABLE_UFW_IF_INACTIVE="${ENABLE_UFW_IF_INACTIVE:-0}"
SYSTEMD_UNIT="/etc/systemd/system/kodexlink-relay.service"
NGINX_SITE="/etc/nginx/sites-available/kodexlink-relay.conf"
NGINX_SITE_ENABLED="/etc/nginx/sites-enabled/kodexlink-relay.conf"
RELAY_ENV_FILE="${ETC_DIR}/relay.env"
GENERATE_DB_PASSWORD=0
DRY_RUN=0
APP_HOME="/home/${APP_USER}"
NVM_DIR="${APP_HOME}/.nvm"

usage() {
  cat <<'EOF'
用法:
  sudo bash scripts/setup-ubuntu-relay-host.sh \
    --domain relay.example.com \
    --ssl-cert-path /path/fullchain.pem \
    --ssl-key-path /path/privkey.pem \
    [--db-password strong-password] \
    [--dry-run]

可选参数:
  --node-major 24
  --app-user kodexlink
  --app-group kodexlink
  --app-dir /srv/kodexlink
  --etc-dir /etc/kodexlink
  --log-dir /var/log/kodexlink
  --relay-port 8787
  --db-name codex_mobile
  --db-user kodexlink
  --db-password strong-password
  --ssh-ufw-profile OpenSSH
  --enable-ufw-if-inactive 0|1
  --dry-run
EOF
}

while [[ $# -gt 0 ]]; do
  case "$1" in
    --domain)
      RELAY_DOMAIN="${2:-}"
      shift 2
      ;;
    --ssl-cert-path)
      SSL_CERT_PATH="${2:-}"
      shift 2
      ;;
    --ssl-key-path)
      SSL_KEY_PATH="${2:-}"
      shift 2
      ;;
    --node-major)
      NODE_MAJOR="${2:-}"
      shift 2
      ;;
    --app-user)
      APP_USER="${2:-}"
      shift 2
      ;;
    --app-group)
      APP_GROUP="${2:-}"
      shift 2
      ;;
    --app-dir)
      APP_DIR="${2:-}"
      shift 2
      ;;
    --etc-dir)
      ETC_DIR="${2:-}"
      RELAY_ENV_FILE="${ETC_DIR}/relay.env"
      shift 2
      ;;
    --log-dir)
      LOG_DIR="${2:-}"
      shift 2
      ;;
    --relay-port)
      RELAY_PORT="${2:-}"
      shift 2
      ;;
    --db-name)
      DB_NAME="${2:-}"
      shift 2
      ;;
    --db-user)
      DB_USER="${2:-}"
      shift 2
      ;;
    --db-password)
      DB_PASSWORD="${2:-}"
      shift 2
      ;;
    --ssh-ufw-profile)
      SSH_UFW_PROFILE="${2:-}"
      shift 2
      ;;
    --enable-ufw-if-inactive)
      ENABLE_UFW_IF_INACTIVE="${2:-}"
      shift 2
      ;;
    --dry-run)
      DRY_RUN=1
      shift
      ;;
    --help|-h)
      usage
      exit 0
      ;;
    *)
      echo "[setup] 未知参数: $1" >&2
      usage
      exit 1
      ;;
  esac
done

log() {
  printf '[setup] %s\n' "$1"
}

dry_log() {
  printf '[setup][dry-run] %s\n' "$1"
}

fail() {
  printf '[setup][ERROR] %s\n' "$1" >&2
  exit 1
}

require_root() {
  if [[ "${DRY_RUN}" == "1" ]]; then
    dry_log "dry-run 模式跳过 root 权限要求"
    return
  fi
  if [[ "${EUID}" -ne 0 ]]; then
    fail "请使用 root 或 sudo 运行"
  fi
}

validate_name() {
  local value="$1"
  local label="$2"
  if [[ ! "${value}" =~ ^[a-zA-Z_][a-zA-Z0-9_]*$ ]]; then
    fail "${label} 格式非法: ${value}"
  fi
}

escape_sql_literal() {
  printf "%s" "$1" | sed "s/'/''/g"
}

ensure_required_inputs() {
  [[ -n "${RELAY_DOMAIN}" ]] || fail "缺少 --domain / RELAY_DOMAIN"
  [[ -n "${SSL_CERT_PATH}" ]] || fail "缺少 --ssl-cert-path / SSL_CERT_PATH"
  [[ -n "${SSL_KEY_PATH}" ]] || fail "缺少 --ssl-key-path / SSL_KEY_PATH"
  validate_name "${DB_NAME}" "DB_NAME"
  validate_name "${DB_USER}" "DB_USER"

  if [[ -z "${DB_PASSWORD}" ]]; then
    if [[ "${DRY_RUN}" == "1" ]]; then
      DB_PASSWORD="<auto-generated-on-real-run>"
    else
      GENERATE_DB_PASSWORD=1
    fi
  fi
}

run_preflight_check() {
  log "执行安装前环境检查"
  bash "${CHECK_SCRIPT}" \
    --phase preflight \
    --domain "${RELAY_DOMAIN}" \
    --ssl-cert-path "${SSL_CERT_PATH}" \
    --ssl-key-path "${SSL_KEY_PATH}" \
    --app-user "${APP_USER}" \
    --app-dir "${APP_DIR}" \
    --etc-dir "${ETC_DIR}" \
    --relay-port "${RELAY_PORT}" \
    --db-name "${DB_NAME}" \
    --db-user "${DB_USER}" \
    --node-major "${NODE_MAJOR}"
}

install_base_packages() {
  log "安装基础依赖"
  if [[ "${DRY_RUN}" == "1" ]]; then
    dry_log "将执行: apt-get update"
    dry_log "将执行: apt-get install -y ca-certificates curl gnupg lsb-release apt-transport-https software-properties-common git build-essential ufw"
    return
  fi

  apt-get update
  DEBIAN_FRONTEND=noninteractive apt-get install -y \
    ca-certificates \
    curl \
    gnupg \
    lsb-release \
    apt-transport-https \
    software-properties-common \
    git \
    build-essential \
    ufw

  if [[ "${GENERATE_DB_PASSWORD}" == "1" ]]; then
    DB_PASSWORD="$(openssl rand -hex 24)"
    log "未提供 DB_PASSWORD，已自动生成随机密码"
  fi
}

install_nodejs() {
  log "通过 nvm 安装 Node.js ${NODE_MAJOR}"
  if [[ "${DRY_RUN}" == "1" ]]; then
    dry_log "将以用户 ${APP_USER} 安装 nvm 到 ${NVM_DIR}"
    dry_log "将执行: curl -o- https://raw.githubusercontent.com/nvm-sh/nvm/v0.40.4/install.sh | bash"
    dry_log "将执行: source ${NVM_DIR}/nvm.sh"
    dry_log "将执行: nvm install ${NODE_MAJOR}"
    dry_log "将执行: corepack enable"
    dry_log "将执行: corepack prepare pnpm@${PNPM_VERSION} --activate"
    return
  fi

  runuser -u "${APP_USER}" -- /bin/bash -lc "export NVM_DIR='${NVM_DIR}'; mkdir -p \"\$NVM_DIR\"; curl -o- https://raw.githubusercontent.com/nvm-sh/nvm/v0.40.4/install.sh | bash"
  runuser -u "${APP_USER}" -- /bin/bash -lc "export NVM_DIR='${NVM_DIR}'; . \"\$NVM_DIR/nvm.sh\" && nvm install ${NODE_MAJOR} && nvm alias default ${NODE_MAJOR} && corepack enable && corepack prepare pnpm@${PNPM_VERSION} --activate"
}

install_server_packages() {
  log "安装 Nginx / PostgreSQL / Redis"
  if [[ "${DRY_RUN}" == "1" ]]; then
    dry_log "将执行: apt-get install -y nginx postgresql postgresql-contrib redis-server redis-tools"
    dry_log "将执行: systemctl enable nginx postgresql redis-server"
    dry_log "将执行: systemctl restart nginx postgresql redis-server"
    return
  fi

  DEBIAN_FRONTEND=noninteractive apt-get install -y \
    nginx \
    postgresql \
    postgresql-contrib \
    redis-server \
    redis-tools

  systemctl enable nginx
  systemctl enable postgresql
  systemctl enable redis-server
  systemctl restart nginx
  systemctl restart postgresql
  systemctl restart redis-server
}

create_system_user_and_dirs() {
  log "创建系统用户与目录"
  if [[ "${DRY_RUN}" == "1" ]]; then
    dry_log "将确保系统组存在: ${APP_GROUP}"
    dry_log "将确保系统用户存在: ${APP_USER}"
    dry_log "将创建目录: ${APP_DIR}"
    dry_log "将创建目录: ${ETC_DIR}"
    dry_log "将创建目录: ${LOG_DIR}"
    return
  fi

  if ! getent group "${APP_GROUP}" >/dev/null 2>&1; then
    groupadd --system "${APP_GROUP}"
  fi

  if ! id "${APP_USER}" >/dev/null 2>&1; then
    useradd --system --gid "${APP_GROUP}" --create-home --shell /bin/bash "${APP_USER}"
  fi

  install -d -m 0755 -o "${APP_USER}" -g "${APP_GROUP}" "${APP_DIR}"
  install -d -m 0755 -o root -g "${APP_GROUP}" "${ETC_DIR}"
  install -d -m 0755 -o "${APP_USER}" -g "${APP_GROUP}" "${LOG_DIR}"
}

configure_postgresql() {
  log "配置 PostgreSQL 数据库与账号"
  local escaped_password
  escaped_password="$(escape_sql_literal "${DB_PASSWORD}")"

  if [[ "${DRY_RUN}" == "1" ]]; then
    dry_log "将执行: 创建或更新 PostgreSQL 角色 ${DB_USER}"
    dry_log "将执行: 创建数据库 ${DB_NAME}，owner=${DB_USER}"
    return
  fi

  runuser -u postgres -- psql postgres <<SQL
DO \$\$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = '${DB_USER}') THEN
    CREATE ROLE ${DB_USER} LOGIN PASSWORD '${escaped_password}';
  ELSE
    ALTER ROLE ${DB_USER} WITH LOGIN PASSWORD '${escaped_password}';
  END IF;
END
\$\$;
SQL

  if ! runuser -u postgres -- psql postgres -tAc "SELECT 1 FROM pg_database WHERE datname = '${DB_NAME}'" | grep -q '^1$'; then
    runuser -u postgres -- createdb --owner="${DB_USER}" "${DB_NAME}"
  fi
}

write_relay_env() {
  log "写入 Relay 生产环境变量文件"
  if [[ "${DRY_RUN}" == "1" ]]; then
    dry_log "将写入文件: ${RELAY_ENV_FILE}"
    cat <<EOF

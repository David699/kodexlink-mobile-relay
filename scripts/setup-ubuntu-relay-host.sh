configure_ufw() {
  if ! command -v ufw >/dev/null 2>&1; then
    log "UFW 未安装，跳过防火墙配置"
    return
  fi

  log "配置 UFW 规则"
  if [[ "${DRY_RUN}" == "1" ]]; then
    dry_log "将执行: ufw allow ${SSH_UFW_PROFILE}"
    dry_log "将执行: ufw allow Nginx Full"
    if [[ "${ENABLE_UFW_IF_INACTIVE}" == "1" ]]; then
      dry_log "若 UFW 未激活，将执行: ufw enable"
    else
      dry_log "若 UFW 未激活，将不会自动启用"
    fi
    return
  fi

  ufw allow "${SSH_UFW_PROFILE}" >/dev/null
  ufw allow 'Nginx Full' >/dev/null

  local status
  status="$(ufw status 2>/dev/null | head -n 1 || true)"
  if [[ "${status}" == "Status: active" ]]; then
    ufw reload >/dev/null
    log "UFW 已激活，已刷新规则"
    return
  fi

  if [[ "${ENABLE_UFW_IF_INACTIVE}" == "1" ]]; then
    yes | ufw enable >/dev/null
    log "UFW 原本未激活，已按要求启用"
  else
    log "UFW 当前未激活，未自动启用；如需启用请手动执行: ufw enable"
  fi
}

